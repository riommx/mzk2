import '../validator/failure.dart';

// ################################################
// #  Ver: 3.0 - last: 20/01/23
// #  Nullsafety
// #  Interface Class for Validated Value Objects
// ################################################
abstract class Value<T> {
  // ==============================================
  bool get valid;

  // ==============================================
  Iterable<Failure> get failures;

  // ==============================================
  T getOr({required T replace});

  // ==============================================
  /// Throws [ValueError]
  /// containing the [failures]
  T get getOrCrash;
}
